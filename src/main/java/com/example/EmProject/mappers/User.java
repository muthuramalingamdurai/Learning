package com.example.EmProject.mappers;

public class User {
    // Private fields
    private String name;
    private String role;
    private Number age;

    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for role
    public String getRole() {
        return role;
    }

    // Setter for role
    public void setRole(String role) {
        this.role = role;
    }

    // Getter for age
    public Number getAge() {
        return age;
    }

    // Setter for age
    public void setAge(Number age) {
        this.age = age;
    }
}
